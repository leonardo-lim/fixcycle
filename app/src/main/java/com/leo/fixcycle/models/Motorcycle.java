package com.leo.fixcycle.models;

public class Motorcycle {
    int image,cylinderCapacity;
    String name,licensePlate,brand,type,fuelType,color,date;

    public Motorcycle(int image, int cylinderCapacity, String name, String licensePlate, String brand, String type, String fuelType, String color, String date) {
        this.image = image;
        this.cylinderCapacity = cylinderCapacity;
        this.name = name;
        this.licensePlate = licensePlate;
        this.brand = brand;
        this.type = type;
        this.fuelType = fuelType;
        this.color = color;
        this.date = date;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public int getCylinderCapacity() {
        return cylinderCapacity;
    }

    public void setCylinderCapacity(int cylinderCapacity) {
        this.cylinderCapacity = cylinderCapacity;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLicensePlate() {
        return licensePlate;
    }

    public void setLicensePlate(String licensePlate) {
        this.licensePlate = licensePlate;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getFuelType() {
        return fuelType;
    }

    public void setFuelType(String fuelType) {
        this.fuelType = fuelType;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
